    const minutes = totalMinutes % 60;
    
    // –ü—Ä–∞–≤–∏–ª—å–Ω—ã–µ —Å–∫–ª–æ–Ω–µ–Ω–∏—è
    const hourForm = getCorrectForm(hours, ['—á–∞—Å', '—á–∞—Å–∞', '—á–∞—Å–æ–≤']);
    const minuteForm = getCorrectForm(minutes, ['–º–∏–Ω—É—Ç—É', '–º–∏–Ω—É—Ç—ã', '–º–∏–Ω—É—Ç']);
    
    // –§–æ—Ä–º–∏—Ä—É–µ–º —Ç–µ–∫—Å—Ç –≤—Ä–µ–º–µ–Ω–∏
    let timeText = `${hours} ${hourForm}`;
